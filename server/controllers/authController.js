// Login User Logic
exports.loginUser = async (req, res) => {
  const { email, password, role } = req.body; // 1. Get role from request body

  try {
    // Check if user exists
    const user = await User.findOne({ email });
    if (!user) {
      return res.status(400).json({ msg: 'Invalid credentials' });
    }

    // 2. NEW: Check if the selected role matches the user's role in the database
    if (user.role !== role) {
      return res.status(401).json({ msg: 'Role mismatch. Please select your correct role.' });
    }

    // Compare entered password with stored hashed password
    const isMatch = await bcrypt.compare(password, user.password);
    if (!isMatch) {
      return res.status(400).json({ msg: 'Invalid credentials' });
    }

    // If credentials are correct, send back a token
    res.json({
      _id: user.id,
      name: user.name,
      email: user.email,
      role: user.role,
      token: generateToken(user._id),
    });
  } catch (err) {
    console.error(err.message);
    res.status(500).send('Server Error');
  }
};